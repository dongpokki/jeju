package kr.spot.vo;

public class SpotGoodVO {
	private int spot_num;
	private int user_num;
	private int spot_good;
	public int getSpot_num() {
		return spot_num;
	}
	public void setSpot_num(int spot_num) {
		this.spot_num = spot_num;
	}
	public int getUser_num() {
		return user_num;
	}
	public void setUser_num(int user_num) {
		this.user_num = user_num;
	}
	public int getSpot_good() {
		return spot_good;
	}
	public void setSpot_good(int spot_good) {
		this.spot_good = spot_good;
	}
}
